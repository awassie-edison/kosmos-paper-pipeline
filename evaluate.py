# Full-text fetching
# ---------------------------------------------------------------------------

def _fetch_epmc_xml(pmcid: str) -> str | None:
    """Fetch full-text XML from Europe PMC (works for PMC open-access papers)."""
    if not pmcid:
        return None
    cfg = get_config()
    url = f"{cfg.api_endpoints.europepmc_fulltext}/{pmcid}/fullTextXML"
    try:
        resp = requests.get(url, timeout=cfg.timeouts.api_request)
        if resp.status_code == 200 and len(resp.text) > 1000:
            return resp.text
    except requests.RequestException:
        pass
    return None


def _fetch_publisher_html(doi: str) -> str | None:
    """Fetch HTML from the publisher via DOI redirect."""
    if not doi:
        return None
    cfg = get_config()
    url = f"https://doi.org/{doi}"
    try:
        resp = requests.get(
            url,
            headers={"Accept": "text/html", "User-Agent": "KosmosPipeline/1.0"},
            timeout=cfg.timeouts.api_request,
            allow_redirects=True,
        )
        if resp.status_code == 200 and len(resp.text) > 2000:
            # Strip script/style tags to reduce token count
            from bs4 import BeautifulSoup
            soup = BeautifulSoup(resp.text, "html.parser")
            for tag in soup(["script", "style", "nav", "footer", "header"]):
                tag.decompose()
            return soup.get_text(separator="\n", strip=True)
    except Exception:
        pass
    return None


def fetch_full_text(doi: str, pmcid: str = "") -> tuple[str, str]:
    """Return (text, source) where source is 'xml', 'html', or 'abstract'."""
    xml = _fetch_epmc_xml(pmcid)
    if xml:
        return xml, "xml"

    html = _fetch_publisher_html(doi)
    if html:
        return html, "html"

    return "", "abstract"


