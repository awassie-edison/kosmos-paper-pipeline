"""Step 3: Fetch full text and evaluate papers via the Claude API.

Each candidate paper is:
  1. Full-text fetched (Europe PMC XML → publisher HTML → abstract-only fallback)
  2. Sent to Claude with the evaluation rubric
  3. Assessed via tool_use for structured output
"""

